# Find the Python Documentation on operator precedence, 
# and use it to determine the result of evaluating 4 * 5 + 3**2 / 10.

result = 4 * 5 + 3**2 / 10
print(result)

result = 4 * 5 + 9 / 10
print(result)

result = 20 + 9 / 10
print(result)

result = 20 + 0.9
print(result)

result = 20.9
print(result)
